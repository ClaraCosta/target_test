INDICE = 13   # Armazenando valor 13 dentro da variável INDICE
SOMA = 0      # Variável SOMA vazia
K = 0         # Variável K vazia

while K < INDICE:   # Incrementando valor em K, até que esteja maior que INDICE
    K += 1
    SOMA += K

print(f"Valor final de SOMA: {SOMA}")
